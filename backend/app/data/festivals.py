# ── Helper functions ──────────────────────────────────────────────────────────

def get_festivals_for_country(country: str) -> list[dict]:
    """Return festivals for a country plus MULTI entries."""
    festivals: list[dict] = list(FESTIVALS.get(country.upper(), []))
    festivals.extend(FESTIVALS.get("MULTI", []))
    return festivals


def get_active_festivals(country: str, check_date: date) -> list[dict]:
    """Return all festivals active on check_date for a country."""
    check_str = check_date.isoformat()
    return [
        f for f in get_festivals_for_country(country)
        if f["start_date"] <= check_str <= f["end_date"]
    ]


def get_upcoming_festival(country: str, check_date: date, days_ahead: int = 7) -> Optional[dict]:
    """Return the nearest festival starting within days_ahead days (exclusive of today)."""
    candidates: list[tuple[int, dict]] = []
    for f in get_festivals_for_country(country):
        try:
            start = date.fromisoformat(f["start_date"])
            delta = (start - check_date).days
            if 0 < delta <= days_ahead:
                candidates.append((delta, f))
        except ValueError:
            continue
    return min(candidates, key=lambda x: x[0])[1] if candidates else None


def get_recently_ended_festivals(country: str, check_date: date) -> list[dict]:
    """Return festivals that ended within their recovery_days window before check_date."""
    result = []
    for f in get_festivals_for_country(country):
        try:
            end = date.fromisoformat(f["end_date"])
            recovery = f.get("recovery_days", 3)
            days_since_end = (check_date - end).days
            if 0 < days_since_end <= recovery:
                result.append({**f, "_days_since_end": days_since_end})
        except ValueError:
            continue
    return result


def compute_festival_adjustment(
    festival: Optional[dict],
    base_goal: int,
    festival_mode: str,
) -> Optional[dict]:
    """
    Build the festival_adjustment response dict.
    Returns None when mode is 'off' or no festival is active.
    """
    if festival is None or festival_mode == "off":
        return None

    original_goal = base_goal
    adjusted_goal = (
        round(base_goal * festival["goal_multiplier"])
        if festival_mode == "full"
        else original_goal
    )

    return {
        "festival_id": festival["id"],
        "festival_name": festival["name"],
        "original_goal": original_goal,
        "adjusted_goal": adjusted_goal,
        "type": festival["type"],
        "emoji": festival["emoji"],
        "color_accent": festival["color_accent"],
        "ambient_effect": festival["ambient_effect"],
        "description": festival["description"],
        "goal_multiplier": festival["goal_multiplier"],
        "festival_mode": festival_mode,
        "start_date": festival["start_date"],
        "end_date": festival["end_date"],
    }
